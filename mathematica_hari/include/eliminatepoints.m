EliminatePoints[pts_, OptionsPattern[]] := 
    Module[{pmat, qmat, target, x, seed, v, t0, t, targetLog, prob, iter, 
      fluc, temp, temp0, best, tbest, flexindices, stack, stackindex, 
      seqtrial}, pmat = Outer[OptionValue[ProfileFunction][#1, #2, 
          OptionValue[ProfileParameters]] & , pts, pts, 1]; 
      qmat = SparseArray[2*DiagonalMatrix[Diagonal[pmat]] - pmat]; 
      target[x_] := x . qmat . x; seed = OptionValue[Seed]; 
      If[Head[seed] =!= List, seed = (seed & ) /@ pts]; 
      Scan[(seed[[#1]] = 1) & , OptionValue[FixedPointIndices]]; 
      flexindices = RandomSample[Complement[Range[Length[pts]], 
         OptionValue[FixedPointIndices]]]; t0 = target[seed]; tbest = t0; 
      best = seed; targetLog = {{0, t0}}; iter = 1; seqtrial = 0; 
      stackindex = 0; temp0 = OptionValue[InitialTemp]; 
      Print[ProgressIndicator[Dynamic[iter], 
        {0, OptionValue[MaxIterations]}]]; 
      While[iter < OptionValue[MaxIterations], 
       temp = temp0*Exp[(iter/OptionValue[MaxIterations])*
            Log[OptionValue[FinalTemp]]]; If[stackindex == 0, 
         fluc = flexindices[[Mod[seqtrial, Length[flexindices]] + 1]]; 
          seqtrial++, fluc = stack[[stackindex]]; stackindex--; ]; v = seed; 
        v[[fluc]] = 1 - v[[fluc]]; t = target[v]; If[t >= t0, 
         seed = v; t0 = t, prob = Exp[-(t0 - t)/temp]; If[Random[] < prob, 
           If[t0 > tbest, best = seed; tbest = t0]; seed = v; t0 = t; 
            If[stackindex == 0 && OptionValue[VicinityStack], 
             stack = qmat[[fluc]] + qmat[[All,fluc]]; stack = SortBy[
                Most[ArrayRules[stack]], -#1[[2]] & ]; If[Length[stack] >= 
                2, stack = Rest[stack][[All,1,1]]; stack = Select[stack, 
                  MemberQ[flexindices, #1] & ]; stackindex = Length[
                  stack]; ]; ]; ]; ]; AppendTo[targetLog, {iter, t0}]; 
        iter++; ]; {Pick[pts, best /. {0 -> False, 1 -> True}], targetLog}]
 
